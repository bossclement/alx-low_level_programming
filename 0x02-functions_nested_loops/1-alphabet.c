void print_alphabet(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)
	{
		_putchar(c);
	}
}

/**
 * main - main func
 * Description: blablabla
 * Return: 0
 */

void main(void)
{
	print_alphabet();
}
