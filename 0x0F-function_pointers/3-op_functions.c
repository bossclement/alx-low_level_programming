#include "3-calc.h"

/**
 * op_add - function
 * @a: number 1
 * @b: number 2
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * @a: number 1
 * @b: number 2
 * Return: difference btn 2 numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function
 * @a: number 1
 * @b: number 2
 * Return: product btn 2 numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function
 * @a: number 1
 * @b: number 2
 * Return: result of division btn 2 numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function
 * @a: number 1
 * @b: number 2
 * Return: remainder of the division btn 2 numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

